def aire_cercle(rayon):
    """Prends le rayon (m), retourne l'aire du cerlce (m^2)"""
    return 3.14*rayon**2


def multiplier(a, b):
    '''
    Retourne le produit de deux nombres.
 
            Paramètres:
                    a: Nombre entier ou décimale
                    b: Nombre entier ou décimale
 
            Retourne:
                    prod: Produit de a et b
    '''
    prod = a*b
    return prod

